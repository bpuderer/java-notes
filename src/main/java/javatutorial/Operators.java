package javatutorial;

public class Operators {

	public static void main(String[] args) {
		
		// https://docs.oracle.com/javase/tutorial/java/nutsandbolts/operators.html
		
		int i = 0;
		System.out.println("i = " + i);
		System.out.println("i++ = " + i++);
		System.out.println("i = " + i);
		System.out.println("++i = " + ++i);
		System.out.println("i = " + i);
		
	}
	
}
